package clases;
// Ejercicio 2 Crea una clase Calculadora con métodos sumar, restar, multiplicar y dividir. Úsala desde el main.

public class calculadora {
    
    private int numA;
    private int numB;

    public calculadora(int numA,int numB){
        this.numA = numA;
        this.numB = numB;
    }




    public int suma(){
        int total;
        return total=numA+numB;
    }
    
    public int resta(){
        int total;    
        return total = numA-numB;
    }
        
 }
